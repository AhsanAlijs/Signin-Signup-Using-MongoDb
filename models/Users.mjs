import mongoose from "mongoose";

const { Schema } = mongoose

const usersSchema = new Schema({
    fullname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
})

const User = mongoose.model('users',usersSchema) 

export default User